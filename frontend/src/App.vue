<template>
  <div id="app">
    <div v-if="isLogin">
      <Login/>
    </div>
    <div v-else id="nav" class="sticky-top">
      <MainHeader/>
      <router-view></router-view>
    </div>
  </div>
  
</template>

<script>
import MainHeader from './views/MainHeader.vue'
import Login from './views/login/Login.vue'

export default ({
  name: "App",
  components: {
    Login,
    MainHeader,
  },
  data: function () {
    return {
      isLogin: false,
    }
  },methods: {
    getVideos() {
      this.$store.dispatch('getVideos')
    },
  },
  computed: {
    isLogin: function () {
      return this.$store.getters.isLogin
    },
  },
  created: function () {
    const token = localStorage.getItem('jwt')
    if (token) {
      this.isLogin = true
    }
  },
  mounted: function () {
    this.getVideos()
    this.$router.push({ name: "Main" })
  },
})

</script>

